#include "3-calc.h"

/**
 * op_add - function
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function
 * @a first input
 * @b: second value
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function
 * @a: first value
 * @b: second value
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - functions
 * @a: first value
 * @b: second value
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function
 * @a: first value
 * @b: second value
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
